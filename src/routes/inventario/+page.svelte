    <h1>Política de Privacidad para Mi Inventario</h1>

    <p>En "Mi Inventario", accesible desde [URL de la App], una de nuestras principales prioridades es la privacidad
        de
        nuestros usuarios. Esta Política de Privacidad documenta los tipos de información que recopilamos y registramos
        en
        "Mi Inventario" y cómo la usamos.</p>

    <p>Esta política se aplica solo a nuestras actividades en línea y es válida para los visitantes de nuestra
        aplicación en
        lo que respecta a la información que compartieron y/o recopilan en "Mi Inventario". Esta política no es
        aplicable
        a ninguna información recopilada fuera de línea o a través de canales que no sean esta aplicación.</p>

    <h2>Consentimiento</h2>

    <p>Al usar nuestra aplicación, usted acepta nuestra Política de Privacidad y acepta sus términos.</p>

    <h2>Información que recopilamos</h2>

    <p>La información personal que se pide que proporcione, y las razones por las que se pide que la proporcione, le
        será
        aclarada en el momento en que le pidamos que proporcione su información personal.</p>

    <p>Si nos contacta directamente, podemos recibir información adicional sobre usted, como su nombre, dirección de
        correo
        electrónico, número de teléfono, el contenido del mensaje y/o los adjuntos que nos pueda enviar, y cualquier
        otra
        información que decida proporcionar.</p>

    <p>Cuando se registra para una Cuenta, podemos pedir su información de contacto, incluyendo elementos como nombre,
        nombre de la empresa, dirección, dirección de correo electrónico y número de teléfono.</p>

    <h2>Cómo usamos su información</h2>

    <p>Usamos la información que recopilamos de diversas maneras, incluyendo para:</p>

    <ul class="list-disc pl-8">
        <li>Proporcionar, operar y mantener nuestra aplicación</li>
        <li>Mejorar, personalizar y expandir nuestra aplicación</li>
        <li>Entender y analizar cómo utiliza nuestra aplicación</li>
        <li>Desarrollar nuevos productos, servicios, características y funcionalidades</li>
        <li>Comunicarnos con usted, ya sea directamente o a través de uno de nuestros socios, incluyendo para servicio
            al
            cliente, para proporcionarle actualizaciones y otra información relacionada con la aplicación, y con fines
            de
            marketing y promoción
        </li>
        <li>Enviarle correos electrónicos</li>
        <li>Encontrar y prevenir fraudes</li>
        <li>Inicio de sesión con Firebase Auth</li>
    </ul>

    <p>"Mi Inventario" usa Firebase Auth para facilitar el inicio de sesión seguro. Cuando inicia sesión con su
        correo
        electrónico y contraseña, Firebase Auth procesa sus credenciales y nosotros no almacenamos su contraseña en
        nuestros
        servidores.</p>

    <h2>Gestión de su Información de Inventario</h2>

    <p>"Mi Inventario" recopila y almacena información relacionada con su inventario de productos cosméticos,
        incluyendo
        la cantidad de productos que tiene de cada tipo. Esta información se utiliza exclusivamente para ayudarle a
        administrar su inventario y no se comparte con terceros sin su consentimiento explícito.</p>

    <h2>Seguridad de la Información</h2>

    <p>La seguridad de su información personal es importante para nosotros, pero recuerde que ningún método de
        transmisión
        por Internet o método de almacenamiento electrónico es 100% seguro. Aunque nos esforzamos por usar medios
        comercialmente aceptables para proteger su información personal, no podemos garantizar su seguridad
        absoluta.</p>

    <h2>Enlaces a Otros Sitios</h2>

    <p>Nuestra aplicación puede contener enlaces a otros sitios que no son operados por nosotros. Si hace clic en un
        enlace
        de terceros, será dirigido al sitio de ese tercero. Le recomendamos encarecidamente que revise la Política de
        Privacidad de cada sitio que visite.</p>

    <h2>Cambios en Esta Política de Privacidad</h2>

    <p>Nos reservamos el derecho de actualizar o cambiar nuestra Política de Privacidad en cualquier momento y debe
        revisar
        esta Política de Privacidad periódicamente.</p>

