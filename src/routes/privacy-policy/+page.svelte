    <h1>Política de Privacidad</h1>

    <p>
        En <u>@soyllamas</u>, una de nuestras principales prioridades es la privacidad
        de nuestros usuarios. Esta Política de Privacidad documenta los tipos de
        información que recopilamos y registramos y cómo la usamos.
    </p>

    <p>
        Esta política se aplica solo a nuestras actividades en línea y es válida
        para los visitantes de nuestra aplicación en lo que respecta a la
        información que compartieron y/o recopilan en <u>@soyllamas</u>. Esta
        política no es aplicable a ninguna información recopilada fuera de línea o a
        través de canales que no sean esta aplicación.
    </p>

    <h2>Consentimiento</h2>

    <p>
        Al usar nuestra aplicación, usted acepta nuestra Política de Privacidad y
        acepta sus términos.
    </p>

    <h2>Información que recopilamos</h2>

    <p>
        Como un chatbot automatizado que opera a través de WhatsApp, recopilamos la
        información que usted proporciona directamente a través de sus conversaciones
        con el chatbot. Esto puede incluir texto, imágenes y otros tipos de
        contenido que usted envíe.
    </p>

    <h2>Cómo usamos su información</h2>

    <p>Usamos la información que recopilamos de diversas maneras, incluyendo para:</p>

    <ul class="list-disc pl-8">
        <li>
            Proporcionar respuestas a sus preguntas y solicitudes a través del
            chatbot.
        </li>
        <li>
            Utilizar la API de OpenAI para procesar sus mensajes y generar respuestas
            personalizadas.
        </li>
        <li>Mejorar y personalizar la experiencia del usuario con el chatbot.</li>
        <li>
            Analizar el uso del chatbot para entender mejor las necesidades de los
            usuarios y mejorar su funcionalidad.
        </li>
    </ul>

    <h2>Compartir información con OpenAI</h2>

    <p>
        Tenga en cuenta que al utilizar nuestro chatbot, la información que usted
        proporciona será procesada por OpenAI a través de su API. El uso de esta
        información por parte de OpenAI está sujeto a sus propios términos de
        servicio y política de privacidad. Le recomendamos que revise estos
        documentos para comprender cómo OpenAI utiliza su información.
    </p>

    <h2>Seguridad de la Información</h2>

    <p>
        La seguridad de su información personal es importante para nosotros.
        Implementamos medidas de seguridad razonables para proteger su información
        contra el acceso no autorizado, la divulgación o la alteración. Sin
        embargo, tenga en cuenta que ningún método de transmisión por Internet o
        método de almacenamiento electrónico es 100% seguro, y no podemos garantizar
        la seguridad absoluta de su información.
    </p>

